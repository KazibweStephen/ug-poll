    {
        //
        $save_District=new District();
        $save_District->name=$request->name;
        $save_District->region_id=$request->region_id;
        try {
            $save_District->save();
            $status="Operation successfull";
        } catch (\Exception $e) {
            $status=$e->getMessage();
        }

        return redirect()->back()->with(['status'=>$status]);

    }

